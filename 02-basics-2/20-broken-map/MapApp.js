import { defineComponent, reactive } from 'vue'

export default defineComponent({
  name: 'MapApp',

  setup() {
    // –†–µ–∞–∫—Ç–∏–≤–Ω—ã–π –æ–±—ä–µ–∫—Ç —Å –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º–∏ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç –º–µ—Ç–∫–∏
    const styleObj = reactive({
      top: 0,
      left: 0,
    })

    /**
     * –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–ª–∏–∫–∞ –ø–æ –∫–∞—Ä—Ç–µ –¥–ª—è —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç –º–µ—Ç–∫–∏
     * @param {MouseEvent} event
     */
    function handleClick(event) {
      styleObj.top = `${event.offsetY}px`
      styleObj.left = `${event.offsetX}px`
    }

    return {
      styleObj,

      handleClick,
    }
  },

  template: /* html */`
    <div class="map" @click="handleClick">
      <img class="map-image" src="./map.png" alt="Map" draggable="false" />
      <span class="pin" :style="styleObj">üìç</span>
    </div>
  `,
})
